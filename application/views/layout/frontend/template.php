                <h1><strong>PROLOG</strong></h1>
                <b>Komite Olahraga Masyarakat Indonesia (KORMI)</b> adalah
                lembaga yang menaungi berbagai induk olahraga (Inorga) rekreasi
                di Indonesia. Olahraga rekreasi adalah salah satu jenis olahraga
                yang diatur dalam UU Nomor 5 Tahun 2005 tentang Sistem
                Keolahragaan Nasional. Dalam UU tersebut, sistem keolahragaan
                nasional dibagi menjadi olahraga pendidikan, olahraga prestasi
                dan olahraga rekreasi. Atlet olahraga rekreasi sering disebut
                sebagai pegiat olahraga. KORMI merupakan anggota TAFISA,
                organisasi internasional yang menyebarluaskan gerakan Sport for
                All di dunia yang bertujuan agar olahraga dilakukan oleh semua
                orang dari segala usia dan tingkatan ekonomi. TAFISA bersama
                KORMI (saat itu bernama FORMI) dan Kemenpora menyelenggarakan
                TAFISA World Games edisi ke-6 pada tahun 2016 di Jakarta.
              </div>
              <p></p>
              <div class="content ps-0 ps-lg-5">
                <h1><strong>SEJARAH</strong></h1>
                <b>KORMI</b> bermula dari
                <b>Federasi Olahraga Masyarakat Indonesia (FOMI)</b> yang
                didirikan oleh beberapa induk organisasi olahraga. Seiring
                berkembangnya waktu dan dinamika di Pemerintahan, FOMI pun
                bertransformasi menjadi
                <b>Federasi Olahraga Rekreasi Masyarakat Indonesia (FORMI)</b>
                dan pada tahun 2020 kembali melakukan perubahan menjadi
                <b>Komite Olahraga Rekreasi Masyarakat Indonesia</b> atau
                <b>KORMI</b> dan ditahun 2022 melakukan perubahan lagi dengan
                menghapuskan kata Rekreasi dan menjadi Komite Olahraga
                Masyarakat Indonesia melalui proses Musyawarah Nasional Luar
                Biasa (Munaslub) yang diselenggarakan pada hari Rabu 22 Februari
                2023.
